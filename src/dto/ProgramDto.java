package dto;

/**
 * The interface Program dto.
 */
public interface ProgramDto {

  /**
   * Gets id.
   *
   * @return the id
   */
  public int getId();

  /**
   * Sets id.
   *
   * @param id the id
   */
  public void setId(int id);

  /**
   * Gets name.
   *
   * @return the name
   */
  public String getName();

  /**
   * Sets name.
   *
   * @param name the name
   */
  public void setName(String name);

  /**
   * Gets description.
   *
   * @return the description
   */
  public String getDescription();

  /**
   * Sets description.
   *
   * @param description the description
   */
  public void setDescription(String description);

  /**
   * Gets ver nr.
   *
   * @return the ver nr
   */
  public int getVerNr();

  /**
   * Sets ver nr.
   *
   * @param verNr the ver nr
   */
  public void setVerNr(int verNr);

}
